let currentLang = 'FR';

function applyTranslations(lang) {
  const t = TRANSLATIONS[lang];
  if (!t) return;
  currentLang = lang;

  /* Direction RTL pour l'arabe */
  document.documentElement.setAttribute('dir', lang === 'AR' ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang.toLowerCase());

  /* Appliquer chaque clé */
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  /* Sauvegarder le choix */
  try { localStorage.setItem('ar_lang', lang); } catch(e){}
}

function initLang() {
  let saved = 'FR';
  try { saved = localStorage.getItem('ar_lang') || 'FR'; } catch(e){}
  /* Détecter la langue du navigateur */
  if (saved === 'FR') {
    const bl = (navigator.language || 'fr').toUpperCase().slice(0,2);
    const map = { FR:'FR', EN:'EN', ES:'ES', PT:'PT', AR:'AR', ZH:'ZH', RU:'RU', DE:'DE', JA:'JA', HI:'HI' };
    if (map[bl]) saved = map[bl];
  }
  applyTranslations(saved);

  /* Mettre à jour le sélecteur visuel */
  const flagMap = { FR:'🇫🇷', EN:'🇬🇧', ES:'🇪🇸', PT:'🇧🇷', AR:'🇸🇦', ZH:'🇨🇳', RU:'🇷🇺', DE:'🇩🇪', JA:'🇯🇵', HI:'🇮🇳' };
  const activFlag = document.getElementById('activFlag');
  const activCode = document.getElementById('activCode');
  if (activFlag) activFlag.textContent = flagMap[saved] || '🇫🇷';
  if (activCode) activCode.textContent = saved;

  document.querySelectorAll('.lang-option').forEach(o => {
    o.classList.toggle('active', o.getAttribute('data-lang') === saved);
  });
}

/* Appelé depuis le HTML quand on clique sur une langue */
function setLang(code, flag, el) {
  document.getElementById('activCode').textContent = code;
  document.getElementById('activFlag').textContent = flag;
  document.querySelectorAll('.lang-option').forEach(o => o.classList.remove('active'));
  el.classList.add('active');
  document.getElementById('langSel').classList.remove('open');
  applyTranslations(code);
}

function toggleLang() {
  document.getElementById('langSel').classList.toggle('open');
}

document.addEventListener('click', e => {
  const sel = document.getElementById('langSel');
  if (sel && !sel.contains(e.target)) sel.classList.remove('open');
});

/* Initialiser immédiatement car le script est chargé après le DOM */
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLang);
} else {
  initLang();
}
